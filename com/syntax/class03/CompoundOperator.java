package com.syntax.class03;

public class CompoundOperator {
	public static void main(String[] args) {

		int num = 100;
		num = num + 100;
		System.out.println(num);
		
		num = num + 100;
		System.out.println(num);
		
		num+=100;
		System.out.println(num);
		
		num -= 50;
		System.out.println(num);
		
		num /= 7;
		System.out.println(num);
		
		num *= 2;
		System.out.println(num);
		
		num = num * 2;
		System.out.println(num);
		
		
		
	}
}
