package com.syntax.class16;

public class Calculator {


	void add(int a, int b) {

		System.out.println(a + b);
	}

	void sub(int a, int b) {
		System.out.println(a - b);
	}

	void divide(int a, int b) {
		System.out.println(a / b);
	}

	void multiply(int a, int b) {
		System.out.println(a * b);
	}
}
