package com.syntax.class10;

public class TrianglePatternWithNumbers {
public static void main(String [] args) {
	for (int row = 1; row <=5; row++) {
		for (int col = 1; col <= row; col++) {
			System.out.print(row);
		}
		System.out.println();
	}
	
	for (int row = 5; row >=1; row--) {
		for (int col = 1; col<=row; col++) {
			System.out.print(row);
		}
		System.out.println();
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
	
}
